package net.okdi.apiV4.entity;

import java.util.Date;
/**
 * 帖子评论表
 * @author amssy
 *
 */
public class CardComment {

	private Long cardId;//帖子id
	private Long cardCommentId;//此帖子评论id
	private Long fromMemberId;//评论人id
	private Long toMemberId;//被回复人id
	private String fromMemberName;//评论人姓名
	private String toMemberName;//被回复人姓名
	private String commentType;//1:评论  2：回复
	private String content;//评论内容
	private Date commentTime;//创建时间
	public Long getCardId() {
		return cardId;
	}
	public void setCardId(Long cardId) {
		this.cardId = cardId;
	}
	public Long getCardCommentId() {
		return cardCommentId;
	}
	public void setCardCommentId(Long cardCommentId) {
		this.cardCommentId = cardCommentId;
	}
	public Long getFromMemberId() {
		return fromMemberId;
	}
	public void setFromMemberId(Long fromMemberId) {
		this.fromMemberId = fromMemberId;
	}
	public Long getToMemberId() {
		return toMemberId;
	}
	public void setToMemberId(Long toMemberId) {
		this.toMemberId = toMemberId;
	}
	public String getFromMemberName() {
		return fromMemberName;
	}
	public void setFromMemberName(String fromMemberName) {
		this.fromMemberName = fromMemberName;
	}
	public String getToMemberName() {
		return toMemberName;
	}
	public void setToMemberName(String toMemberName) {
		this.toMemberName = toMemberName;
	}
	public String getCommentType() {
		return commentType;
	}
	public void setCommentType(String commentType) {
		this.commentType = commentType;
	}
	public Date getCommentTime() {
		return commentTime;
	}
	public void setCommentTime(Date commentTime) {
		this.commentTime = commentTime;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
	
}
