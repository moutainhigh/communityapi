package net.okdi.api.entity;

import java.util.Date;
/**
 * @ClassName MemberContactComm
 * @Description 联系人通讯信息类
 * @author mengnan.zhang
 * @date 2014-10-28
 * @since jdk1.6
 */
public class MemberContactComm {
    //主键
    private Long id;
    //通讯信息所属类型ID
    private Long commTagId;
    //联系人ID   
    private Long contactId;
    //通讯号码   
    private String commCode;
    //是否为默认 
    private Short isDefault;
    //创建时间   
    private Date createTime;
    
    public Long getId() {
        return id;
    }

   
    public void setId(Long id) {
        this.id = id;
    }

   
    public Long getCommTagId() {
        return commTagId;
    }

   
    public void setCommTagId(Long commTagId) {
        this.commTagId = commTagId;
    }

 
    public Long getContactId() {
        return contactId;
    }

    
    public void setContactId(Long contactId) {
        this.contactId = contactId;
    }

   
    public String getCommCode() {
        return commCode;
    }

  
    public void setCommCode(String commCode) {
        this.commCode = commCode;
    }

   
    public Short getIsDefault() {
        return isDefault;
    }

   
    public void setIsDefault(Short isDefault) {
        this.isDefault = isDefault;
    }

   
    public Date getCreateTime() {
        return createTime;
    }

    
    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }
}