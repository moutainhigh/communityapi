package net.okdi.api.entity;

public class NetOpenPrice {
    private Long id;

    private Long fromAddressId;

    private Long toAddressId;

    private Short firstFreight;

    private Short continueFreight;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getFromAddressId() {
        return fromAddressId;
    }

    public void setFromAddressId(Long fromAddressId) {
        this.fromAddressId = fromAddressId;
    }

    public Long getToAddressId() {
        return toAddressId;
    }

    public void setToAddressId(Long toAddressId) {
        this.toAddressId = toAddressId;
    }

    public Short getFirstFreight() {
        return firstFreight;
    }

    public void setFirstFreight(Short firstFreight) {
        this.firstFreight = firstFreight;
    }

    public Short getContinueFreight() {
        return continueFreight;
    }

    public void setContinueFreight(Short continueFreight) {
        this.continueFreight = continueFreight;
    }
}