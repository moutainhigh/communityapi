package net.okdi.api.entity;
/**
 * @ClassName MemberAddressTypeTagInfo
 * @Description 地址标签
 * @author mengnan.zhang
 * @date 2014-10-28
 * @since jdk1.6
 */
public class MemberAddressTypeTagInfo {
    //主键
    private Long id;
    //持有人ID
    private Long memberId;
    //地址标签名
    private String addressTypeName;

    public Long getId() {
        return id;
    }
    
    public void setId(Long id) {
        this.id = id;
    }
  
    public Long getMemberId() {
        return memberId;
    }
   
    public void setMemberId(Long memberId) {
        this.memberId = memberId;
    }
 
    public String getAddressTypeName() {
        return addressTypeName;
    }

    public void setAddressTypeName(String addressTypeName) {
        this.addressTypeName = addressTypeName;
    }
    public MemberAddressTypeTagInfo(){}
	public MemberAddressTypeTagInfo(Long id, Long memberId,
			String addressTypeName) {
		super();
		this.id = id;
		this.memberId = memberId;
		this.addressTypeName = addressTypeName;
	}
    
}