package net.okdi.api.vo;

public class VO_ExpressPrice {

	private String firstFreight;// 首重价格

	private String continueWeight;// 续重价格

	private String province;// 派件省份名称

	public String getFirstFreight() {
		return firstFreight;
	}

	public void setFirstFreight(String firstFreight) {
		this.firstFreight = firstFreight;
	}

	public String getContinueWeight() {
		return continueWeight;
	}

	public void setContinueWeight(String continueWeight) {
		this.continueWeight = continueWeight;
	}

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

}
