package net.okdi.apiV2.entity;

import java.util.Date;

public class DailyRewardActivity {

private long activityId;//主键,活动编号
	
	private String province;//省份
	
	private Short siteFlag;//站点修改标识
	
	private Double siteRewardPrice;//站点奖励价格
	
	private Integer siteRewardNum;//站点奖励数量
	
	private Short pointFlag;//代收点修改标识
	
	private Double pointRewardPrice;//代收点奖励价格
	
	private Integer pointRewardNum;//代收点奖励数量

	private Date createTime;//创建时间
	
	private Date updateTime;//更新时间
	
	
	public long getActivityId() {
		return activityId;
	}

	public void setActivityId(long activityId) {
		this.activityId = activityId;
	}

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

	public Short getSiteFlag() {
		return siteFlag;
	}

	public void setSiteFlag(Short siteFlag) {
		this.siteFlag = siteFlag;
	}

	public Double getSiteRewardPrice() {
		return siteRewardPrice;
	}

	public void setSiteRewardPrice(Double siteRewardPrice) {
		this.siteRewardPrice = siteRewardPrice;
	}

	public Integer getSiteRewardNum() {
		return siteRewardNum;
	}

	public void setSiteRewardNum(Integer siteRewardNum) {
		this.siteRewardNum = siteRewardNum;
	}

	public Short getPointFlag() {
		return pointFlag;
	}

	public void setPointFlag(Short pointFlag) {
		this.pointFlag = pointFlag;
	}

	public Double getPointRewardPrice() {
		return pointRewardPrice;
	}

	public void setPointRewardPrice(Double pointRewardPrice) {
		this.pointRewardPrice = pointRewardPrice;
	}

	public Integer getPointRewardNum() {
		return pointRewardNum;
	}

	public void setPointRewardNum(Integer pointRewardNum) {
		this.pointRewardNum = pointRewardNum;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public Date getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}
	
	
}
