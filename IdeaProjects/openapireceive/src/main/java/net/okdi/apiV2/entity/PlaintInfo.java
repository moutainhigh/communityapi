package net.okdi.apiV2.entity;

import java.util.Date;

public class PlaintInfo {

	private Long id; //主键
	
	private Long memberId; //申请人id
	
	private Long compId; //公司id
	
	private String compName; //公司名称
	
	private Short compType; //类型 站点 营业分部
	
	private Long plaintPhone; //申诉人手机号
	
	private Short status; //状态 同意 驳回
	
	private Date createTime; //创建时间
	
	private String responsible; //负责人姓名
	
	private String idNum; //身份证号
	
	private Short rejectReason; //审核拒绝原因
	
	private String auditDesc; //备注信息
	
	private String business; //营业执照
	
	

	public String getBusiness() {
		return business;
	}

	public void setBusiness(String business) {
		this.business = business;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getMemberId() {
		return memberId;
	}

	public void setMemberId(Long memberId) {
		this.memberId = memberId;
	}

	public Long getCompId() {
		return compId;
	}

	public void setCompId(Long compId) {
		this.compId = compId;
	}

	public String getCompName() {
		return compName;
	}

	public void setCompName(String compName) {
		this.compName = compName;
	}

	public Short getCompType() {
		return compType;
	}

	public void setCompType(Short compType) {
		this.compType = compType;
	}

	public Long getPlaintPhone() {
		return plaintPhone;
	}

	public void setPlaintPhone(Long plaintPhone) {
		this.plaintPhone = plaintPhone;
	}

	public Short getStatus() {
		return status;
	}

	public void setStatus(Short status) {
		this.status = status;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public String getResponsible() {
		return responsible;
	}

	public void setResponsible(String responsible) {
		this.responsible = responsible;
	}

	public String getIdNum() {
		return idNum;
	}

	public void setIdNum(String idNum) {
		this.idNum = idNum;
	}

	public Short getRejectReason() {
		return rejectReason;
	}

	public void setRejectReason(Short rejectReason) {
		this.rejectReason = rejectReason;
	}

	public String getAuditDesc() {
		return auditDesc;
	}

	public void setAuditDesc(String auditDesc) {
		this.auditDesc = auditDesc;
	}
	
}
