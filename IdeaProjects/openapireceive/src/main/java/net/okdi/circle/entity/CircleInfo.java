package net.okdi.circle.entity;

import java.util.Date;

//圈子信息
public class CircleInfo {

	private long circleId;//圈子主键
	
	private String circleName;//圈子名称
	
	private String circleLogoUrl;//圈子logo
	
	private String circleIntroduce;//圈子介绍
	
	private String circleAddress;//圈子地址
	
	private Double circleLongitude;//圈子经度
	
	private Double circleLatitude;//圈子维度
	
	private long circleMember;//圈子成员memberId集合
	
	private String circleNotice;//圈子公告
	
	private Date createTime;//创建时间
	
	private long circleCreaterId;//创建者id
	
	private String circleCreaterName;//创建者姓名
	
	private long circleMasterId;//群主id
	
	private String circleMasterName;//群主姓名
	
	private Short circleStatus;//圈子状态0:待审核, 1:通过审核,2 审核被拒绝

	
	
	public long getCircleId() {
		return circleId;
	}

	public void setCircleId(long circleId) {
		this.circleId = circleId;
	}

	public String getCircleName() {
		return circleName;
	}

	public void setCircleName(String circleName) {
		this.circleName = circleName;
	}

	public String getCircleLogoUrl() {
		return circleLogoUrl;
	}

	public void setCircleLogoUrl(String circleLogoUrl) {
		this.circleLogoUrl = circleLogoUrl;
	}

	public String getCircleIntroduce() {
		return circleIntroduce;
	}

	public void setCircleIntroduce(String circleIntroduce) {
		this.circleIntroduce = circleIntroduce;
	}

	public String getCircleAddress() {
		return circleAddress;
	}

	public void setCircleAddress(String circleAddress) {
		this.circleAddress = circleAddress;
	}

	public Double getCircleLongitude() {
		return circleLongitude;
	}

	public void setCircleLongitude(Double circleLongitude) {
		this.circleLongitude = circleLongitude;
	}

	public Double getCircleLatitude() {
		return circleLatitude;
	}

	public void setCircleLatitude(Double circleLatitude) {
		this.circleLatitude = circleLatitude;
	}

	public long getCircleMember() {
		return circleMember;
	}

	public void setCircleMember(long circleMember) {
		this.circleMember = circleMember;
	}

	public String getCircleNotice() {
		return circleNotice;
	}

	public void setCircleNotice(String circleNotice) {
		this.circleNotice = circleNotice;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public long getCircleCreaterId() {
		return circleCreaterId;
	}

	public void setCircleCreaterId(long circleCreaterId) {
		this.circleCreaterId = circleCreaterId;
	}

	public String getCircleCreaterName() {
		return circleCreaterName;
	}

	public void setCircleCreaterName(String circleCreaterName) {
		this.circleCreaterName = circleCreaterName;
	}

	public long getCircleMasterId() {
		return circleMasterId;
	}

	public void setCircleMasterId(long circleMasterId) {
		this.circleMasterId = circleMasterId;
	}

	public String getCircleMasterName() {
		return circleMasterName;
	}

	public void setCircleMasterName(String circleMasterName) {
		this.circleMasterName = circleMasterName;
	}

	public Short getCircleStatus() {
		return circleStatus;
	}

	public void setCircleStatus(Short circleStatus) {
		this.circleStatus = circleStatus;
	}
	
}
